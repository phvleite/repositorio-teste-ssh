function nomeCapital(capitalEstado, estado) {
  if (capitalEstado[1] === estado) {
      capital = capitalEstado[2];
  }
}

function geraListaCidade(cidade) {
  const uf = document.getElementById('uf').value;
  if (cidade[1] === uf) {
    const cidOp = document.getElementById('cidade');
    const cidNome = document.createElement('option');
    cidNome.innerText = cidade[0];
    cidNome.value = cidade[0];
    cidOp.appendChild(cidNome);
    if (cidNome.innerText === capital) {
        cidNome.setAttribute('selected', 'selected');
    }
  }
}

function optionUfCid() {
    const cidOp = document.getElementById('cidade');
    while (cidOp.firstChild) {
        cidOp.firstChild.remove(cidOp.firstChild);
    }
    ufCidLista.forEach((cidade) => geraListaCidade(cidade));
}

function geraListaUf(estado) {
    const ufOp = document.getElementById('uf');
    const ufNome = document.createElement('option');
    ufNome.innerText = estado[1];
    ufNome.value = estado[1];
    ufNome.setAttribute('class', 'estado');
    ufOp.appendChild(ufNome);
}

function optionsUf() {
    ufLista.forEach(estado => geraListaUf(estado));
}

